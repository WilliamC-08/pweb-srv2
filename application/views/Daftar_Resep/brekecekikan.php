    <h2>Brekecek Ikan</h2>
    <p>Brekecek adalah Makanan Khas kota Cilacap. Semua jenis ikan laut bisa di masak brekecek tapi biasanya yg sering di masak ikan kembung, cangkek dan ikan layur, yang penting jenis ikan yg dagingnya tidak terlalu tebal agar bumbunya meresap.
    Selain enak dan banyak di gemari ikan laut juga baik bagi kesehatan dan kecerdasan otak.</p>


    <h3>Bahan-bahan:</h3>
      <ul>
        <li>1 ekor ikan (bebas sesuai selera)</li>
        <li>1 batang serai</li>
        <li>2 lembar daun bawang</li>
        <li>3 lembar daun salam</li>
        <li>Gula merah secukupnya</li>
        <li>1 tomat</li>
        <li>2 cabai merah besar</li>
        <li>Penyedap rasa secukupnya</li>
        <li>Air secukupnya</li>
      </ul>
    <h3>Bumbu halus:</h3>
      <ul>
        <li>1 sdt garam</li>
        <li>3 biji kemiri</li>
        <li>7 cabai rawit</li>
        <li>3 cabai merah</li>
        <li>5 siung bawang merah</li>
        <li>Lada bubuk secukupnya</li>
        <li>3 siung bawang putih</li>
        <li>2 cm kunyit</li>
        <li>2 cm jahe</li>
      </ul>
    <h3>Cara memasak:</h3>
      <ol>
        <li>Goreng ikan hingga matang, lalu sisihkan.</li>
        <li>Sangrai kemiri, kunyit, bawang putih dan juga jahe.</li>
        <li>Tumis bumbu halus, tambahkan gula merah dan tomat. Aduk hingga bumbu harum dan matang. Tuangkan air secukupnya .</li>
        <li>Masukkan daun jeruk, daun bawang, irisan cabai merah besar, dan daun bawang.</li>
        <li>Masukkan ikan, aduk sampai mendidih. Tambahkan bumbu penyedap secukupnya.</li>
        <li>Sajikan selagi masih hangat.</li>
      </ol>
