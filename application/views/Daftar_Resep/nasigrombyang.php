    <h2>Nasi Grombyang</h2>
    <p>Nasi Grombyang adalah makanan yang berasal dari Pemalang, Jawa Tengah.
    Nama 'grombyang' sendiri diartikan dengan bergoyang. Goyang yang dimaksud pada makanan ini adalah kuahnya yang menutupi nasinya sehingga membuat isi mangkuk seakan-akan bergoyang.</p>

    <h3>Bahan-bahan:</h3>
      <ul>
        <li>2.500 ml air</li>
        <li>200 gram babat sapi,, rebus potong-potong sesuai selera</li>
        <li>2 lembar daun salam</li>
        <li>150 gram usus sapi, direbus, potong-potong sesuai selera</li>
        <li>300 gram daging tetelan</li>
        <li>1 sdt gula apsir</li>
        <li>2 batang serai, ambil putihnya saja, lalu memarkan</li>
        <li>2 batang daun bawang, iris tipis</li>
        <li>3 sdt gula merah</li>
        <li>1/4 sdt garam</li>
        <li>3 sdm minyak untuk menumis</li>
      </ul>

    <h3>Bumbu halus:</h3>
      <ul>
        <li>1 sdt gula pasir</li>
        <li>2 batang serai, diambil putihnya, dimemarkan</li>
        <li>2 batang daun bawang, iris tipis sesuai selera</li>
        <li>3 sdt gula merah</li>
        <li>1/4 sdt garam</li>
        <li>3 sdm minyak untuk menumis</li>
      </ul>
    <h3>Bumbu halus:</h3>
      <ul>
        <li>1 sdt ketumbar</li>
        <li>2 buah keluwek, diseduh</li>
        <li>1/2 sdt merica</li>
        <li>8 butir bawang merah</li>
        <li>3 siung bawang putih</li>
        <li>2 cm kunyit, dibakar</li>
        <li>100 gram kelapa parut, disangrai</li>
        <li>1 cm jahe, dibakar</li>
      </ul>
    <h3>Bahan sambal:</h3>
      <ul>
      <li>10 buah cabai rawit hijau</li>
      <li>1/4 sdt garam</li>
      <li>2 siung bawang putih</li>
      <li>1/4 sdt gula pasir</li>
      </ul>
    <h3>Pelengkap:</h3>
      <ul>
      <li>1.000 gram nasi putih</li>
      <li>2 sdm bawang merah goreng</li>
      <li>3 lembar kubis, diiris</li>
      </ul>
    <h3>Cara memasak:</h3>
      <ol>
        <li>Rebus tetelan, serai, dan daun salam sampai matang.</li>
        <li>Tumis bumbu halus, tuangkan ke dalam rubusan daging. Kemudian masukkan babat, usus, gula merah dan garam.</li>
        <li>Sebelum diangkat, beri daun bawang. Aduk sampai rata.</li>
        <li>Untuk sambalnya, rebhus cabai rawit dan bawang putih. Angkat, beri garam dan gula pasir, kemudian uleg.</li>
      </ol>
