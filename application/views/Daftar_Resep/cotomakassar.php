    <h2>Coto Makassar</h2>
    <p>Coto Makassar merupakan salah satu makanan yang bisa dimasak sendiri di rumah. Sebab, bumbu coto Makassar mudah diracik sendiri. Apa saja ya bumbunya?
       Makanan khas Makassar, Sulawesi Selatan ini memang menjadi salah satu soto nusantara yang enak untuk dinikmati. Bayangkan saja, kuah kental asal kacang serta aroma kaldu sapi menjadi satu, yumm, sangat lezat!
    </p>


    <h3>Bahan:</h3>
      <ul>
        <li>1 kg daging sandur lamur sapi dan jeroan sesuai selera</li>
        <li>3 liter air</li>
        <li>1 sdt garam</li>
        <li>4 sdm minyak sayur</li>
      </ul>

    <h3>Haluskan:</h3>
      <ul>
        <li>8 siung bawang putih</li>
        <li>sangrai 2 sdm kacang tanah</li>
        <li>lengkuas kupas</li>
        <li>cincang 3 batang serai (pilih yang putih dan agak lunak)</li>
        <li>iris halus 1 sdm ketumbar</li>
        <li>sangrai 1/2 sdt jintan</li>
        <li>sangrai 1 sdt merica butiran</li>
        <li>sangrai 2 sdt garam</li>
      </ul>

    <h3>Resep sambal coto Makassar asli (taoco):</h3>
      <ul>
        <li>2 sdm minyak sayur</li>
        <li>125 ml air</li>
        <li>1,5 sdm gula pasir</li>
      </ul>

    <h3>Haluskan:</h3>
      <ul>
        <li>5 buah cabai merah</li>
        <li>4 buah cabai rawit</li>
        <li>4 sdm taoco yang bagus</li>
      </ul>

    <h3>Bumbu pelengkap:</h3>
      <ul>
        <li>2 sdm daun bawang dan iris halus</li>
        <li>2 sdm seledri dan iris halus</li>
        <li>3 sdm bawang merah goreng</li>
        <li>3 sdm jeruk nipis</li>
      </ul>

    <h3>Cara membuat coto Makassar:</h3>
      <ol>
        <li>Rebus daging dan jeroan secara terpisah hingga empuk. Tiriskan daging dan jeroan dan potong dadu 1,5 cm.</li>
        <li>Buang kaldu jeroan, ukur kaldu daging sebanyak 2,5 liter.</li>
        <li>Panaskan minyak, tumis bumbu halus hingga wangi, dan angkat.</li>
        <li>Masukkan ke dalam panci berisi kaldu, tambahkan daging dan jeroan. Masak di atas api kecil hingga bumbu meresap. Angkat</li>
        <li>lalu sajikan coto Makassar di atas mangkuk dengan bumbu pelengkap dan sambal taoco.</li>
      </ol>
