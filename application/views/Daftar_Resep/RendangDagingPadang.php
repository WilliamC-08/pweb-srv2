        <h2>RENDANG DAGING PADANG</h2>
        <p>Salah satu resep masakan Indonesia yang selalu membuat ketagihan adalah rendang Padang. Siapa yang tidak kenal dengan rendang daging yang satu ini, kita semua tentu sudah tahu betapa lezat dan harumnya masakan ini.</p>

        <h3>Bahan:</h3>
          <ul>
            <li>500g daging sapi, potong kotak jadi 8 atau 10 bagian</li>
            <li>600ml santan kental, dari satu setengah butir kelapa</li>
            <li>5 sdm minyak goreng</li>
            <li>300ml air</li>
          </ul>
        <h3>Bumbu Halus:</h3>
          <ul>
            <li>10 buah cabai merah iris</li>
            <li>6 butir bawang merah</li>
            <li>3 siung bawang putih</li>
            <li>3 cm kunyit</li>
            <li>3 cm jahe</li>
            <li>3 cm lengkuas</li>
            <li>1 sdm garam</li>
            <li>2 lembar daun kunyit</li>
            <li>1 batang serai</li>
            <li>2 butir asam kandis</li>
          </ul>
        <h3>Cara Membuat:</h3>
          <ol>
            <li>Tumis bumbu hingga harum, masukkan daging, aduk hingga berubah warna.</li>
            <li>Masukkan daun kunyit, serai dan asam kandis. Aduk perlahan.</li>
            <li>Masukkan air. Masak hingga air menyusut. Aduk perlahan sesekali.</li>
            <li>Masukkan santan, masak dengan api sedang.</li>
            <li>Biarkan hingga daging empuk, kuah kering, dan berminyak. Angkat, sajikan.</li>
          </ol>
