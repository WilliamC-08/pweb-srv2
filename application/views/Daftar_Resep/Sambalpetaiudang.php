      <h2>Sambal Petai Udang</h2>
        <p>Resep makanan Indonesia identik dengan pedas, apalagi yang ini, sabal petai udang. Bagi yang tidak suka pedas, cabainya bisa di kurangi.</p>

      <h3>Bahan:</h3>
        <ul>
          <li>350g udang. dibersihkan, kupas kulitnya, dan biarkan ekornya utuh.</li>
          <li>100g petai, bersihkan, belah</li>
          <li>3 buah tomat ukuran sedang, buang bijinya dan potong</li>
          <li>2 sdm santan kental</li>
          <li>2 sdm kecap manis</li>
          <li>1/2 sdt garam</li>
          <li>11 buah cabai rawit</li>
          <li>4 sdm minyak goreng</li>
          <li>1sdm gula pasir</li>
          <li>150ml cangkir air</li>
        </ul>
      <h3>Bumbu dihaluskan:</h3>
        <ul>
          <li>80g cabai merah</li>
          <li>11 buah bawang merah</li>
          <li>4 siung bawang putih</li>
          <li>2 butir kemiri</li>
        </ul>
      <h3>Cara Memasak</h3>
        <ol>
          <li>Goreng petai sampai warnanya berubah menjadi hijau cerah - kurang lebih selama 1 menit. Angkat dan sisihkan.</li>
          <li>Tumis bumbu yang dihaluskan, masukkan 1 sdm gula pasir. Biarkan sekitar 2 atau 3 menit sampai bumbu mengeluarkan bau harum.</li>
          <li>Masukkan tomat, lalu masak selama 1 menit</li>
          <li>Tuangkan santan dan biarkan sampai mendidih, kira-kira selama 3 menit.</li>
          <li>Masukkan kecap manis dan garam. Lanjutkan memasak selama sekitar 10-15 menit hingga tomat menjadi benar benar hancur.</li>
          <li>Masukkan petai dan cabai rawit, masak selama 3 menit.</li>
          <li>Masukkan udang, masak hingga matang.</li>
          <li>Tuang dalam piring dan sajikan.</li>
        </ol>
