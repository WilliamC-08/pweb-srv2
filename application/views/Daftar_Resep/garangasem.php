    <h2>Garang Asem</h2>
    <p>Garang asem adalah masakan olahan ayam yang dimasak menggunakan daun pisang dan didominasi oleh rasa asam dan pedas. Garang asem berasal dari Grobogan namun kini garang asem populer di Kudus, bahkan kini Garang Asem ada di beberapa kota di provinsi Jawa Tengah memiliki makanan tradisional ini. Antara lain Semarang, Demak, Kudus, Pati, dan Pekalongan.</p>

    <h3>Bahan-bahan:</h3>
      <ul>
        <li>1 kg ayam</li>
        <li>5 siung bawang putih, geprek halus</li>
      </ul>

    <h3>Bumbu:</h3>
      <ul>
        <li>4 buah cabai hijau plompong, potong-potong sesuai selera</li>
        <li>2 lembar daun salam</li>
        <li>7 buah cabai rawit</li>
        <li>5 buah tomat hijau, potong-potong</li>
        <li>3 cm lengkuas</li>
        <li>Santan cair secukupnya</li>
      </ul>
    <h3>Cara memasak:</h3>
      <ol>
        <li>Rebus ayam hingga menjadi empuk.</li>
        <li>Masukkan semua bumbu, masak hingga ayam setengah matang</li>
        <li>Tuangkan santan cair, beri gula dan garam secukupnya. Masak hingga matang dan siap untuk disajikan.</li>
      </ol>
