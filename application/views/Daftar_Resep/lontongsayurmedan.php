    <h2>Lontong Sayur Medan</h2>
      <p>Lontong sayur khas Sumatra Utara ini punya keunikan dalam bumbu sayur gurihnya. Dengan tambahan udang plus tauco, sayur gurihnya bercita rasa unik. Makin seru setelah dilengkapi dengan aneka lauk lainnya.</p>

      <h3>Bahan-bahan:</h3>
        <ul>
        <li>3 butir bawang merah</li>
        <li>3 siung bawang putih</li>
        <li>10 buah cabai hijau</li>
        <li>Daun salam</li>
        <li>Laos</li>
        <li>Sereh</li>
        <li>300 ml santan kara</li>
        <li>Wortel</li>
        <li>Tahu</li>
        <li>Labu siam</li>
        <li>Tempe</li>
        </ul>

      <h3>Bumbu halus:</h3>
        <ul>
        <li>5 siung bawang merah</li>
        <li>5 siung bawang putih</li>
        <li>10 buah cabai merah</li>
        </ul>
      <h3>Cara membuat:</h3>
        <ul>
        <li>Tumis semua bahan</li>
        <li>Masukkan bumbu halus, daun salam, sereh, dan terakhir tahu tempe</li>
        <li>Masukkan air, saat mendidih masukan sayur</li>
        <li>Setelah sedikit empuk, masukkan santan, aduk terus agar tidak pecah</li>
        <li>Koreksi rasa dan sajikan.</li>
        </ul>
