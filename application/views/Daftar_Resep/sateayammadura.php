      <h2>Sate Ayam Madura</h2>
        <p>Sate memang mudah dibeli, tetapi mungkin Anda iseng ingin membuatnya sendiri di rumah. Cobalah resep masakan indonesia khas madura yang satu ini.</p>

      <h3>Bahan:</h3>
        <ul>
          <li>250g fillet ayam, dipotong dadu, kemudian tusukkan ke tusukan sate</li>
          <li>1 buah jeruk nipis yang telah diiris</li>
          <li>100ml minyak goreng</li>
          <li>3 butir bawang merah, diiris tipis</li>
          <li>5 sdm kecap manis</li>
          <li>nasi putih atau bisa diganti lontong secukupnya</li>
        </ul>
      <h3>Bumbu Kacang:</h3>
        <ul>
          <li>3 butir bawang putih</li>
          <li>4 butir bawang merah</li>
          <li>100g kacang tahan, digoreng</li>
          <li>2 butir kemiri</li>
          <li>garam secukupnya</li>
          <li>150ml air kaldu ayam</li>
          <li>2sdm minyak goreng</li>
        </ul>
      <h3>Sambal:</h3>
        <ul>
          <li>cabe merah kecil 15 buah</li>
          <li>bawang putih 4 siung</li>
          <li>garam secukupnya</li>
          <li>minyak goreng secukupnya</li>
        </ul>
      <h3>Cara membuat:</h3>
        <ol>
          Pembuatan bumbu kacang, haluskan bawang putih & merah, kacang tanah, garam, dan juga kemiri. Tumis bumbu kacang hingga tercium bau harum, kemudian masukkan air kaldu dan selanjutnya masak sampai mengental lalu angkat. Sisihkan.
          Selanjutnya, campurkan 2 sendok makan mumbu kacang dengan ditambah 2 sendok makan kecap manis, lalu celupkan sate, kemudian bakar hingga setengah matang. Angkat lalu gulingkan lagi dalam bumbu, terus bakar lagi hingga matang, angkat.
          Untuk sambal bawang, tumis bawang putih sampai setengah matang lalu angkat. Selanjutnya campur bawang putih ditambah juga cabe merah kecil dan garam, haluskan
        </ol>
