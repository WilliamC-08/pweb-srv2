    <h2>Mie Gomak Pedas</h2>
    <p>Mi gomak khas Medan dikenal dengan rasanya yang sangat lezat. Biasanya disajikan dengan bumbu pedas. Kombinasi mi yang dicampur dengan bumbu pedas, dijamin bikin ketagihan.</p>

    <h3>Bahan-bahan:</h3>
      <ul>
        <li>1/2 bungkus mi lidi rebus, tambah sedikit minyak lalu cuci bersih tiriskan</li>
        <li>200 gr kol cuci dan iris-iris</li>
        <li>100 gr wortel cuci dan iris-iris</li>
        <li>Daun bawang, iris-iris</li>
        <li>Seledri, iris-iris</li>
        <li>7 butir bawang merah, iris</li>
        <li>Cabai secukupnya</li>
        <li>Penyedap rasa</li>
        <li>Garam</li>
        <li>Gula</li>
        <li>Kecap sesuai selera</li>
      </ul>
    <h3>Bumbu halus:</h3>
      <ul>
        <li>2 sdt merica atau sesuaikan selera</li>
        <li>2 buah kemiri</li>
        <li>5 siung bawang putih</li>
      </ul>

    <h3>Cara membuat:</h3>
      <ol>
        <li>Tumis bawang merah sampai harum. Masukkan bumbu halus tumis sampai harum</li>
        <li>Masukkan wortel kol daun bawang tumis sampai layu masukkan sedikit air cabai hijau, iris dan aduk</li>
        <li>Masukkan mi lidinya tambahkan garam, gula, penyedap rasa dan kecap manis, lalu aduk rata</li>
        <li>Setelah tanak tes rasa dan masukkan rawit utuh dan daun seledri, aduk dan sajikan.</li>
      </ol>
